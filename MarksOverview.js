import React, { useState, useEffect, useCallback } from 'react';
import axios from 'axios';
import { useNavigate } from 'react-router-dom';
import '../Styles/marksoverview.css';

const MarksOverview = ({ className, subject, onBack }) => {
  const navigate = useNavigate();
  const [overviewData, setOverviewData] = useState([]);
  const [selectedRecord, setSelectedRecord] = useState(null);
  const [marks, setMarks] = useState('');
  const [feedback, setFeedback] = useState('');
  const [batch, setBatch] = useState('');
  const [sortOrder, setSortOrder] = useState('asc');
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState(null);

  const formatDate = (dateString) => {
    if (!dateString) return '';
    const date = new Date(dateString);
    return date.toLocaleDateString('en-US', {
      year: 'numeric',
      month: 'short',
      day: 'numeric'
    });
  };

  const fetchOverviewData = useCallback(async () => {
    setIsLoading(true);
    setError(null);
    try {
      console.log('Fetching data with params:', {
        class: className, 
        subject, 
        batch: batch || 'all', 
        sort: sortOrder 
      });
      
      const res = await axios.get('http://localhost:5000/markOverview', {
        params: { 
          class: className, 
          subject, 
          batch: batch || undefined,
          sort: sortOrder 
        },
      });
      
      // Ensure we always have an array
      const data = Array.isArray(res.data) ? res.data : [];
      setOverviewData(data);
      
      if (data.length === 0) {
        setError('No records found matching your criteria');
      }
    } catch (err) {
      console.error('Fetch error:', {
        message: err.message,
        response: err.response?.data,
        config: err.config
      });
      setError('Failed to fetch data. Please try again.');
      setOverviewData([]);
    } finally {
      setIsLoading(false);
    }
  }, [className, subject, batch, sortOrder]);
  useEffect(() => {
    fetchOverviewData();
  }, [fetchOverviewData]);

  const handleEdit = (record) => {
    setSelectedRecord(record);
    setMarks(record.marks);
    setFeedback(record.feedback || '');
  };

  const handleDelete = async (record) => {
    if (!window.confirm('Are you sure you want to delete this record?')) return;

    try {
      await axios.delete(`http://localhost:5000/marks/${record.id}`);
      fetchOverviewData();
      alert('Record deleted successfully');
    } catch (err) {
      console.error('Delete error:', err);
      alert(err.response?.data?.message || 'Failed to delete record');
    }
  };

  const handleUpdate = async () => {
    if (!selectedRecord) return;
  
    try {
      const response = await axios.put(
        `http://localhost:5000/marks/${selectedRecord.id}`,
        {
          marks: parseFloat(marks), // Ensure numeric value
          feedback: feedback || '',
          testDate: selectedRecord.testDate,
          topic: selectedRecord.topic,
          totalMarks: selectedRecord.totalMarks,
          subject: selectedRecord.subject,
          batch: selectedRecord.batch,
          sub_code: selectedRecord.sub_code,
        }
      );
  
      console.log('Update response:', response.data); // Debug log
      
      // Refresh data immediately after update
      await fetchOverviewData();
      
      setSelectedRecord(null);
      alert('Record updated successfully');
    } catch (err) {
      console.error('Update error:', err.response?.data || err.message);
      alert('Error updating record. See console for details.');
    }
  };
  const handleRefresh = () => {
    fetchOverviewData();
  };

  const handleResetFilters = () => {
    setBatch('');
    setSortOrder('asc');
  };

  if (isLoading) {
    return (
      <div className="loading-container">
        <div className="spinner"></div>
        <p>Loading marks data...</p>
      </div>
    );
  }

  return (
    <div className="overview-container">
      <div className="header">
        <button className="back-button" onClick={onBack || (() => navigate(-1))}>
          Back
        </button>
        <h2>Overview of Marks for {subject}</h2>
      </div>

      <div className="controls">
        <div className="filter-group">
          <label>
            Select Batch:
            <select 
              value={batch} 
              onChange={(e) => setBatch(e.target.value)}
              disabled={isLoading}
            >
              <option value="">All Batches</option>
              <option value="1">Batch 1</option>
              <option value="2">Batch 2</option>
            </select>
          </label>
        </div>

        <div className="sort-group">
          <button
            onClick={() => setSortOrder(prev => (prev === 'asc' ? 'desc' : 'asc'))}
            disabled={isLoading}
          >
            {sortOrder === 'asc' ? '↑ Oldest First' : '↓ Newest First'}
          </button>
        </div>

        <button className="refresh-btn" onClick={handleRefresh} disabled={isLoading}>
          Refresh
        </button>
      </div>

      {error && (
        <div className="message-container error">
          <p>{error}</p>
          <div className="action-buttons">
            <button onClick={handleRefresh}>Retry</button>
            {error.includes('No records') && (
              <button onClick={handleResetFilters}>Reset Filters</button>
            )}
          </div>
        </div>
      )}

      {overviewData.length > 0 ? (
        <div className="table-wrapper">
          <table>
            <thead>
              <tr>
                <th>ID</th>
                <th>Reg No</th>
                <th>Name</th>
                <th>Marks</th>
                <th>Date</th>
                <th>Topic</th>
                <th>Total</th>
                <th>Batch</th>
                <th>Feedback</th>
                <th>Actions</th>
              </tr>
            </thead>
            <tbody>
              {overviewData.map((data) => (
                <tr key={data.id}>
                  <td>{data.id}</td>
                  <td>{data.reg_no}</td>
                  <td>{data.student_name}</td>
                  <td>{data.marks}</td>
                  <td>{formatDate(data.testDate)}</td>
                  <td>{data.topic}</td>
                  <td>{data.totalMarks}</td>
                  <td>{data.batch || '-'}</td>
                  <td>{data.feedback || '-'}</td>
                  <td className="actions">
                    <button 
                      className="edit-btn"
                      onClick={() => handleEdit(data)}
                    >
                      Edit
                    </button>
                    <button
                      className="delete-btn"
                      onClick={() => handleDelete(data)}
                    >
                      Delete
                    </button>
                  </td>
                </tr>
              ))}
            </tbody>
          </table>
          <div className="summary">
            Showing {overviewData.length} records
          </div>
        </div>
      ) : !error && (
        <div className="message-container info">
          <p>No records available for {subject}</p>
          <button onClick={handleRefresh}>Try Again</button>
        </div>
      )}

      {selectedRecord && (
        <div className="modal-overlay">
          <div className="edit-modal">
            <h3>Edit Record</h3>
            <div className="form-group">
              <label>
                Marks:
                <input
                  type="number"
                  value={marks}
                  onChange={(e) => setMarks(e.target.value)}
                  min="0"
                  max={selectedRecord.totalMarks}
                />
              </label>
            </div>
            <div className="form-group">
              <label>
                Feedback:
                <textarea
                  value={feedback}
                  onChange={(e) => setFeedback(e.target.value)}
                  rows="3"
                />
              </label>
            </div>
            <div className="modal-actions">
              <button className="save-btn" onClick={handleUpdate}>
                Save Changes
              </button>
              <button className="cancel-btn" onClick={() => setSelectedRecord(null)}>
                Cancel
              </button>
            </div>
          </div>
        </div>
      )}
    </div>
  );
};

export default MarksOverview;